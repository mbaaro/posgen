public void loadsuppliers(){
    //a function to load the suppliers
    try{
        pst=con.prepareStatement("select `Name` from `suppliers` ");
        rst=pst.executeQuery();
        while(rst.next()){
            jcombo_supplier.addItem(rst.getString("Name"));
        }
    }catch(SQLException ex){
       JOptionPane.showMessageDialog(rootPane, ex);
    }
}

public void updatecashbook(String description,String method,double credit,double debit)
    {
        //declare the variables required by the cashbook
        double credits=0.0;
        double debits=0.0;
        double opening=0.0;
        double balance=0.0;
        int maxid=0;
    try {
        //a function to update the cashbook
        // first get the cashbook details including maxid balance
        pst2=con.prepareStatement("select max(`id`)from `cashbook`");
        rst1=pst2.executeQuery();
        while(rst1.next()){
          maxid=rst1.getInt(1);
        }
        
        
        pst=con.prepareStatement("select * from `cashbook` where `id`='"+maxid+"'");
        rst=pst.executeQuery();
        while(rst.next()){
            /*credits=credits+rst.getFloat("credit");
            debits=debits+rst.getFloat("debit");*/
            opening=rst.getDouble("balance");
        }
        //get opening balance
       // opening=(credits-debits);
        //now get the balance after the transaction
        
      balance=(opening+credit+debit); //we added debit because it is already negative
      JOptionPane.showMessageDialog(rootPane, "the opening balance is "+opening+" the closing balance is "+balance);
        //now lets insert into the cashbook a new entry
        pst1=con.prepareStatement("insert into `cashbook`(`description`,`openingAmount`,`credit`,`debit`,`balance`,`method`)values(?,?,?,?,?,?)");
        pst1.setString(1,description);
        pst1.setDouble(2, opening);
        pst1.setDouble(3,credit );
        pst1.setDouble(4, debit);
        pst1.setDouble(5, balance);
        pst1.setString(6,method );
        pst1.execute();
        JOptionPane.showMessageDialog(rootPane, "cashbook updated successfully");
        
        //clear the table
        
        debit=0;
        credit=0;
        
        
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(rootPane, ex);
    }
    }
