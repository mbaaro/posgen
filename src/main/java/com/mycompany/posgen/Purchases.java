    private void btn_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_saveActionPerformed
         // save the purchase
         payment=jcombo_payment.getSelectedIndex();
    try {
        //first insert into purchaases
        pst=con.prepareStatement("insert into `purchases`(`supplier`,`amount`,`description`,`payment_Method`)values(?,?,?,?)");
        pst.setString(1,jcombo_supplier.getSelectedItem().toString() );
        pst.setString(2,jtf_price.getText());
        pst.setString(3, jta_description.getText());
        pst.setString(4, jcombo_method.getSelectedItem().toString());
        pst.execute();
                //now lets insert into either  the cashbook or the creditors
    } catch (SQLException ex) {
         JOptionPane.showMessageDialog(rootPane, ex);
    }
         //now lets insert into either  the cashbook or the creditors
         if(payment==0){
             //if the payment has been completed
             String method=jcombo_method.getSelectedItem().toString();
             String description=jcombo_supplier.getSelectedItem().toString()+"------->"+jta_description.getText();
             Float debit=Float.valueOf(jtf_price.getText());
             updatecashbook(description, method, 0, debit);
             
            JOptionPane.showMessageDialog(rootPane, " Purchase recorded and cashbook updated");
         }else if(payment==1){
             try {
                 //if payment has not been completed we update the creditors
                 pst1=con.prepareStatement("insert into `creditors` (`creditor`,`amount`,`description`)values (?,?,?)");
                 pst1.setString(1, jcombo_supplier.getSelectedItem().toString());
                 pst1.setString(2, jtf_price.getText());
                 pst1.setString(3, jta_description.getText());
                 pst1.execute();
                  
             JOptionPane.showMessageDialog(rootPane, "Purchase recorded and creditor recorded");
            
             } catch (SQLException ex) {
                 JOptionPane.showMessageDialog(rootPane, ex);
             }
             
             
         }
         
    }//GEN-LAST:event_btn_saveActionPerformed

