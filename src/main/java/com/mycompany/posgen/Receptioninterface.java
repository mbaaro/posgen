    private void btn_signoutActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_signoutActionPerformed
        // signout a room
      String room=jcombo_roomout.getSelectedItem().toString();
      String customer=jcombo_nameout.getSelectedItem().toString();
    try {
        pst=con.prepareStatement("update `rooms` set `status`='0' where `room`='"+room+"'");
        pst.execute();
        
        pst1=con.prepareStatement("update `accomodation` set `status`='0' where `room`='"+room+"' and `customer_name`='"+customer+"' ");
        pst1.execute();
        populatetable();
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(rootPane, ex);
    }
    finally{
        try{
            pst.close();
            rst.close();
        }catch(Exception ex){
         JOptionPane.showMessageDialog(rootPane, ex);
        }
    }
    }//GEN-LAST:event_btn_signoutActionPerformed

