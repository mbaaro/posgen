    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
     try {
         //complete sale without receiving cash
        // complete the cash sale bysaving all the items in the sales database and deleting from
        pst=con.prepareStatement("select * from `basket` where `sale_id`='"+maxid+"'");
       rst=pst.executeQuery();
       while(rst.next()){
           //now let us insert them
          
           pst1=con.prepareStatement("INSERT INTO `sales` (`sale_id`, `product_name`, `unit_cost`, `units_sold`, `total_cost`, `sale_by`,VAT)VALUES(?,?,?,?,?,?,?) ");
          pst1.setInt(1, Integer.valueOf(rst.getString("sale_id")));
           pst1.setString(2, rst.getString("product_name"));
            pst1.setFloat(3,Float.valueOf(rst.getString("unit_cost")) );
           pst1.setFloat(4,Float.valueOf(rst.getString("units_sold")) );
           pst1.setFloat(5,(Float.valueOf(rst.getString("price"))+Float.valueOf(rst.getString("VAT"))) );
           pst1.setString(6,rst.getString("sale_by") );
           pst1.setFloat(7, Float.valueOf(rst.getString("VAT")));
           pst1.execute();
       }
       
       subtractsold();
   printreceipt();  
   //go to the payments form
   //this.dispose();
   
    startthings();
    dtm.setRowCount(0);
    } catch (Exception ex) {
       JOptionPane.showMessageDialog(rootPane, ex);
               
    }
     finally{
         
         try{
            pst3=con.prepareStatement("delete from `basket` where `sale_id`='"+maxid+"'");
            pst3.execute();
         }catch(SQLException ex){
         JOptionPane.showMessageDialog(rootPane, ex);     
         }
     }
    
    }//GEN-LAST:event_jButton4ActionPerformed

