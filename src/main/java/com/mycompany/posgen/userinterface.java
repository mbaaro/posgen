public void subtractsold(){
    
    //subtract the units sold from the total 
  String product,units_sold,currentunits = null;
  Float remaining;
  
        try {
            //get the items from sales
            pst=con.prepareStatement("select * from `sales` where `sale_id`='"+maxid+"'");
            rst=pst.executeQuery();
            while(rst.next()){
             product=rst.getString("product_name");
             units_sold=rst.getString("units_sold");
             //how many are in stock
             pst2=con.prepareStatement("select `number` from `products` where `product_name`='"+product+"'");
             rst2=pst2.executeQuery();
             while(rst2.next()){
               currentunits=rst2.getString("number");
             }
             //change the stock amount
             remaining=(Float.valueOf(currentunits)-Float.valueOf(units_sold));
             //now lets update stock
             pst3=con.prepareStatement("update `products` set `number`='"+remaining+"' where `product_name`='"+product+"'");
             pst3.execute();
             
            }
            
        } catch (SQLException ex) {
           JOptionPane.showMessageDialog(rootPane, ex);
        }
}

