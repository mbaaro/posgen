    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
         // save activity
         String activity=String.valueOf(combo_activity.getSelectedItem());
          units=Float.parseFloat(jtf_units.getText());
         by=String.valueOf(combo_by.getSelectedItem());
         with=jtf_with.getText();
         item=String.valueOf(combo_item.getSelectedItem());
        
         
        try {
            //get the total items in the database and the cost
            pst1=con.prepareStatement("select * from `inventory` where `inventory_item`='"+item+"'");
            rst1=pst1.executeQuery();
            while(rst1.next()){
                cost=rst1.getFloat("unit_cost");
                number=rst1.getFloat("number");
                total_cost=rst1.getFloat("total_cost");
            }
                
        } catch (SQLException ex) {
             JOptionPane.showMessageDialog(rootPane, ex);
        }
         
         if(activity.equalsIgnoreCase("Issue")){
          //   JOptionPane.showMessageDialog(rootPane, number+"units"+units+"remaining"+remainingnumber+"cost"+cost+"totalcost"+totalcost);
                
            try {
                totalcost=(units*cost);
                remainingnumber=(number-units);
                new_total_cost=(remainingnumber*cost);
                // JOptionPane.showMessageDialog(rootPane, remainingnumber+totalcost);
                 //now we update the 2 tables
                pst=con.prepareStatement("INSERT INTO `inventory_ops`( `item_name`, `units_in`, `units_out`, `remaining_units`, `Cost`,  `Transaction_by`, `Transaction_with`) VALUES (?,?,?,?,?,?,?)");
            
                pst.setString(1, item);
                pst.setFloat(2, 0);
                pst.setFloat(3, units);
                pst.setFloat(4,remainingnumber );
                pst.setFloat(5, new_total_cost);
                pst.setString(6, by);
                pst.setString(7, with);
               
                pst.execute();
                
                //updating the inventory
                
                pst2=con.prepareStatement("UPDATE `inventory` SET `number`=?,`total_cost`=? WHERE `inventory_item`=? ");
            pst2.setFloat(1, remainingnumber);
            pst2.setFloat(2, new_total_cost);
            pst2.setString(3, item);
            pst2.execute();
            JOptionPane.showMessageDialog(rootPane, "ITEM ISSUE RECORDED");
            } catch (SQLException ex) {
                 JOptionPane.showMessageDialog(rootPane, ex);
             }
         }else if(activity.equalsIgnoreCase("Receive")){
           totalcost=(units*cost);
                remainingnumber=(number+units);
                new_total_cost=(remainingnumber*cost); 
                try {
                  pst=con.prepareStatement("INSERT INTO `inventory_ops`( `item_name`, `units_in`, `units_out`, `remaining_units`, `Cost`,  `Transaction_by`, `Transaction_with`) VALUES (?,?,?,?,?,?,?)");
            
                pst.setString(1, item);
                pst.setFloat(2, 0);
                pst.setFloat(3, units);
                pst.setFloat(4,remainingnumber );
                pst.setFloat(5, new_total_cost);
                pst.setString(6, by);
                pst.setString(7, with);
               
                pst.execute();
                
                //updating the inventory
                
                pst2=con.prepareStatement("UPDATE `inventory` SET `number`=?,`total_cost`=? WHERE `inventory_item`=? ");
            pst2.setFloat(1, remainingnumber);
            pst2.setFloat(2, new_total_cost);
            pst2.setString(3, item);
            pst2.execute();
            JOptionPane.showMessageDialog(rootPane, "ITEM ADDED TO STOCK");
             } catch (Exception e) {
             JOptionPane.showMessageDialog(rootPane, e);
             }
         }
    }//GEN-LAST:event_jButton1ActionPerformed

