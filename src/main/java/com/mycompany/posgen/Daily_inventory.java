public void loadcombos(){
    //loadig the comboboxes
    try {
        pst=con.prepareStatement("select `inventory_item` from `inventory`");
        rst=pst.executeQuery();
        pst1=con.prepareStatement("select `user_name` from `employees` ");
        rst1=pst1.executeQuery();
        
        while(rst.next()){
            combo_item.addItem(rst.getString("inventory_item"));
        }
        while(rst1.next()){
            combo_by.addItem(rst1.getString("user_name"));
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(rootPane, e);
    }
    finally{
        try {
            pst.close();
            rst.close();
            pst1.close();
            rst1.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e);
        }
    }
    
}
