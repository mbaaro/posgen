    private void btn_equalsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_equalsActionPerformed
        // equals
        if(plusclick>0){
         result=(txtvalue+Float.parseFloat(display.getText()) ) ; 
         display.setText(String.valueOf(result));
         plusclick=0;minusclick=0;divideclick=0;multiplyclick=0;decimalclick=0;
         
        }else if(minusclick>0){
            result=(txtvalue-Float.parseFloat(display.getText()) ) ; 
         display.setText(String.valueOf(result));
         plusclick=0;minusclick=0;divideclick=0;multiplyclick=0;decimalclick=0;
        }else if(divideclick>0){
            result=(txtvalue/Float.parseFloat(display.getText()) ) ; 
         display.setText(String.valueOf(result));
         plusclick=0;minusclick=0;divideclick=0;multiplyclick=0;decimalclick=0;
        }else if(multiplyclick>0){
           result=(txtvalue*Float.parseFloat(display.getText()) ) ; 
         display.setText(String.valueOf(result));
         plusclick=0;minusclick=0;divideclick=0;multiplyclick=0;decimalclick=0; 
        }
        
    }//GEN-LAST:event_btn_equalsActionPerformed

