    public void updatecash(){
        Float sum1 = null,sum2 = null,sum3= null;
        String description="Cash income sales and accomodation";
        String method="CASH";
        try {
            //update cashbook using cash transactions
            pst=con.prepareStatement("Select sum(`total_cost`) from `sales` where `date` < NOW() and `cashbookUpdate`='0' and `method`='CASH'");
        rst=pst.executeQuery();
        while(rst.next()){
            sum1=rst.getFloat(1);
        }
        
        pst1=con.prepareStatement("Select sum(`amount`) from `accomodation` where `date` < NOW() and `cashbookUpdate`='0' and `paymentMethod`='CASH'");
        rst1=pst1.executeQuery();
        while(rst1.next()){
            sum2=rst1.getFloat(1);
        }
        sum3=(sum1+sum2);
         updatecashbook(description, method, sum3, 0);
         
        
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, ex);
        }
        finally{
            try{
              pst.close();
              pst1.close();
              pst2.close();
              rst.close();
              rst1.close();
            }catch(Exception ex){
               JOptionPane.showMessageDialog(rootPane, ex); 
            }
        }
    }
